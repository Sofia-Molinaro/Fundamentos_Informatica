# Si se tiene las variables n1 = 124.25 y n2 = "33.40". Realizar las conversiones
# necesarias para saber la división entera entre ellos y el resto.

n1 = 124.25 
n2 = 33.40
divisionEntera = n1 // n2
restoDeLaDivision = n1 % n2
print(f"El resultado de la división entera de {n1} y {n2} es: {divisionEntera}. \nEl resto de la división de {n1} dividido {n2} es: {restoDeLaDivision}")
